public class DealsItem {
    private int spaImage;
    private String spaName;
    private String spaDate;
    private String spaTitle;

    public int getSpaImage() {
        return spaImage;
    }

    public void setSpaImage(int spaImage) {
        this.spaImage = spaImage;
    }

    public String getSpaName() {
        return spaName;
    }

    public void setSpaName(String spaName) {
        this.spaName = spaName;
    }

    public String getSpaDate() {
        return spaDate;
    }

    public void setSpaDate(String spaDate) {
        this.spaDate = spaDate;
    }

    public String getSpaTitle() {
        return spaTitle;
    }

    public void setSpaTitle(String spaTitle) {
        this.spaTitle = spaTitle;
    }
}
